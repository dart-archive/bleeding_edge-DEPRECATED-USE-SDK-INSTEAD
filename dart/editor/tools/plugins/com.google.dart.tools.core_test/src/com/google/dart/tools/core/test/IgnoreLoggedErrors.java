package com.google.dart.tools.core.test;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Test methods with this annotation will not fail if any errors are logged into the platform log.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.METHOD})
public @interface IgnoreLoggedErrors {
  /**
   * The optional reason why the error is ignored.
   */
  String value() default "";
}
